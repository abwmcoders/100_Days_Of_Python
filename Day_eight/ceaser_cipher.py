alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y',
            'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']


def ceaser(plain_text, shift_number, direction_text):
    cypher_text = ''
    if direction_text == 'decrypt':
        shift_number *= -1
    for letter in plain_text:
        if letter in alphabet:
            position = alphabet.index(letter)
            new_position = position + shift_number
            new_letter = alphabet[new_position]
            cypher_text += new_letter
        else:
            cypher_text += letter    
    print(f"The {direction_text}d text was {cypher_text}")

should_continue = False

while not should_continue:
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    shift = shift % 26
    ceaser(plain_text=text, shift_number=shift, direction_text=direction)
    choice = input("To ontinue type 'yes' else 'no':\n")
    if choice == 'no':
        should_continue = True
